			<h2><a id="projAnchor">Project Management</a></h2>
			<p>We have managed this project using agile management methods, in particular the use of SCRUM. SCRUM is a technique which uses an incremental delivery of iterations to ensure efficient completion of deliverables.</p>
			<p>The reasons that we  have chosen this methodology is because it allows for rapid and flexible development, something that is essential in a research project where the requirements constantly change and the feasibility of many of them is being questioned via continuous research.
			<p>Moreover, in the given timeframe, the adaptiveness and speed of development that is enforced with this method is ideal for our situation. Last but not least, it allows enough freedom between the members to encourage progress.</p>
			<h3>Sprints and iterations</h3>
			<p>To make best use of the SCRUM technique, before beginning the project, we opted to arrange sprint meetings:</p>
			<ul>
				<li>Have a weekly sprint meeting</li>
				<li>Meet every Friday at 14:00</li>
				<li>Meetings lasted around 4 hours</li>
			</ul>
			<h3>Spring planning meeting</h3>
			<p>At each of the sprint meetings, we would begin by showing to each other the work that we have completed since the previous meeting and we would then start reviewing each others work and exchange ideas. This part of the meeting would usually take up to 2 hours.</p>
			<p>Using the knowledge that we had gotten in the first part of the meeting, we would proceed to discuss what we can improve on our methods to increase productivity and we would then start to prioritise the work that needs to be done until the next meeting and then divide it into 3 packages, one for each member of the team. Just like the first part of the meeting, the part would usually last 2 hours as well.</p>
			<h3>Daily Scrum Meeting</h3>
			<p>On top of the Sprint Meetings we would usually have daily scrum meetings on our group on facebook where each member would update the others on the work that he has been doing and any progress or setbacks that he has encountered. This was usually the time that a member would request assistance with his work is he had any major setbacks or he would not finish this work on time. These meeting would not last longer than 15 minutes.</p>
			<p>The reason that we have chosen not to meet in person for the daily meetings was to give the team members more freedom to carry out work on their terms as the face to face meetings would add more pressure that was deemed unnecessary since every member was working diligently. Furthermore, it allowed us to use principles of lean software development as this way we eliminated waste of time and amplified learning by using that meeting time to work and learn.</p>
