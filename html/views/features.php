			<h1>Features</h1>
			<h2><a id="multAnchor">Multi-Way</a></h2>
			<div class="centerMe">
				<img src="assets/img/multiWay.png" width="250">
			</div>
			<p>Our software has built-in support for multi-way conferences. The most recent version of the software allows support for different number of participants so you can now use HydraChat for conferences between 2 and 4 people.</p>
			<h2><a id="visAnchor">Visual interactions</a></h2>
			<p>Typical video conferencing softwares which show multiple participants of the conference on a single screen, dividing the already limited screen space between however many participants there are.
			<p>HydraChat solves this problem by displaying each participant on their own device. This allows conference participants to easily direct attention to another person in the conference. Everyone else is aware of this visual interaction, therefore more natural calls can take place.</p>
			<h2><a id="cloudAnchor">Cloud conference</a></h2>
			<div class="centerMe">
				<img src="assets/img/hydraCall.png" width="400">
			</div>
			<p>No account or sign up necessary. HydraChat is a cloud based web application runnable off any web connected device.</p>
			<p>Conferences are hosted in the cloud, all you need to do to invite participants is share a link.</p>
			<h2><a id="peerAnchor">Peer-to-Peer</a></h2>
			<div class="centerMe">
				<img src="assets/img/serverGraph.png" width="250">
			</div>
			<p>HydraChat is built using WebRTC, an open source project by Google which makes real time communication through the browser a possibility. We use the SimpleWebRTC JavaScript library to deploy WebRTC.</p>
			<p>Part of the advantage of using a system like WebRTC is the Peer-to-peer nature of the technology</p>
			<h2><a id="anonAnchor">Anonymous</a></h2>
			<p>The Peer-to-Peer nature means data transfer is between participants. Whilst our servers set up the call and provide the necessary signalling, none of the words you speak are stored or tracked.</p>
			<h2><a id="freeAnchor">Free</a></h2>
			<p>The HydraChat video conferencing software is currently available free of charge!
