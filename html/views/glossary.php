			<table class="table table-striped table-bordered">
				<thead>
					<tr>
						<th>Term or Acronym</th>
						<th>Definition</th>
					</tr>
				</thead>
				<tbody>
					<tr>
						<td>API</td>
						<td>Application Programming Interface. They are used so that software components can communicate with each other.</td>
					</tr>
					<tr>
						<td>CSS</td>
						<td>Cascading Style Sheets. It is to provide styling features to the webpages defined in HTML.</td>
					</tr>
					<tr>
						<td>DTLS</td>
						<td>Datagram Transport Layer Security. It provides the same authentication services as TLS.</td>
					</tr>
					<tr>
						<td>HTML5</td>
						<td>Hyper-Text Markup Language version 5. It is used to define the structure of web pages. It supports CSS, JavaScript as well as audio and video.</td>
					</tr>
					<tr>
						<td>HTTP</td>
						<td>Hyper-Text Transport Protocol. The protocol used between web server and browser.</td>
					</tr>
					<tr>
						<td>ICE</td>
						<td>Interactive Connectivity Establishment. Also known as hole-punching and it is used to traverse NATs.</td>
					</tr>
					<tr>
						<td>IP</td>
						<td>Internet Protocol. There are two versions, namely version 4 and 6. The network layer protocol that defines the Internet.</td>
					</tr>
					<tr>
						<td>JavaScript</td>
						<td>An interpreted scripting programming language used for writing scripts to be used on web pages. Not to be confused with Java.</td>
					</tr>
					<tr>
						<td>Jingle</td>
						<td>A multimedia signalling protocol.</td>
					</tr>
					<tr>
						<td>NAT</td>
						<td>Network Address Traversal. Function build into routers so that an IP address can be shared by multiple computers under that router.</td>
					</tr>
					<tr>
						<td>Node.js</td>
						<td>A javascript interpreter that contains modules for networking and file access, along with other useful modules, and allows to run Javascript code on a server.</td>
					</tr>
					<tr>
						<td>Peer Connection</td>
						<td>A connection between two endpoints, referred to as peers, that allows the transfer of data. In the context of WebRTC the peers are browsers and the connection is established using the RTCPeerConnection API.</td>
					</tr>
					<tr>
						<td>SIP</td>
						<td>Session Initiation Protocol. A signalling protocol used for communications. It is being used extensively in VoIP implementations.</td>
					</tr>
					<tr>
						<td>SRTP</td>
						<td>Secure Real-Time Transport Protocol. It is the secure version of the RTP protocol and it is used to transport media packets between browsers.</td>
					</tr>
					<tr>
						<td>SDP</td>
						<td>Session Description Protocol. It is used to provide information (description) of the connection established between the peers. The RTCPeerConnection is used to achieve that.</td>
					</tr>
					<tr>
						<td>STUN</td>
						<td>Session Traversal Utilities for NAT. A protocol used for NAT traversal.</td>
					</tr>
					<tr>
						<td>SCTP</td>
						<td>Stream Control Transport Protocol. A protocol used for transport across IP.</td>
					</tr>
					<tr>
						<td>TURN</td>
						<td>Traversal Using Relays around NAT. An extension of STUN that is used for NAT traversal and uses media relays in case anything else fails.</td>
					</tr>
					<tr>
						<td>TLS</td>
						<td>Transport Layer Security. It is used to provide authentication services and older versions of it were known as SSL (Secure Sockets Layer).</td>
					</tr>
					<tr>
						<td>TCP</td>
						<td>Transmission Control Protocol. A protocol that provides reliable transport services.</td>
					</tr>
					<tr>
						<td>UDP</td>
						<td>User Datagram Protocol. It is a protocol used for transport but it is unreliable and thus it is used for small packet exchanges.</td>
					</tr>
					<tr>
						<td>Websocket</td>
						<td>A protocol used to form a bidirectional TCP connection.</td>
					</tr>
				</tbody>
			</table>
